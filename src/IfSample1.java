
public class IfSample1 {

    public static void main(String[] args) {
        int score = 1000; //成績

        //scoreが60以上ならば
        if (score >= 60) {
            //合格です！と表示する
            System.out.println("合格です！");
        }else {
            //不合格です...と表示する
            System.out.println("不合格です...");
        }
    }

}
